package com.pms.entity;

/**
 * 研究所类
 * @author Administrator
 *
 */
public class Institute {
	private int institute_id;
	private String institute_name;
	private String institute_info;
	private String institute_leader;
	public int getInstitute_id() {
		return institute_id;
	}
	public void setInstitute_id(int institute_id) {
		this.institute_id = institute_id;
	}
	public String getInstitute_name() {
		return institute_name;
	}
	public void setInstitute_name(String institute_name) {
		this.institute_name = institute_name;
	}
	public String getInstitute_info() {
		return institute_info;
	}
	public void setInstitute_info(String institute_info) {
		this.institute_info = institute_info;
	}
	public String getInstitute_leader() {
		return institute_leader;
	}
	public void setInstitute_leader(String institute_leader) {
		this.institute_leader = institute_leader;
	}
	
	
	

}
